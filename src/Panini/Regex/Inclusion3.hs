    | l‚ÇÅ ‚äÜ l‚ÇÇ                         -> {-# SCC "match_Letter" #-} Premises [r‚ÇÅ :‚äë r‚ÇÇ]

  -- LetterStar + ElimCat + LeftChoice ----------------------------------------
