    | starChoice1_3                   -> {-# SCC "match_StarChoice_7" #-} Premises [r·¥∏ :‚äë r‚ÇÉ‚ãÖr‚ÇÖ] 
    | starChoice1_4                   -> {-# SCC "match_StarChoice_8" #-} Premises [r·¥∏ :‚äë r‚ÇÑ‚ãÖr‚ÇÖ] 
    | starChoice2                     -> {-# SCC "match_StarChoice_9" #-} Premises [r‚ÇÅ‚ãÖr·¥∏ :‚äë r·¥ø, r‚ÇÇ :‚äë r·¥ø]
