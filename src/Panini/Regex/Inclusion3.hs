    starChoice2   = r·¥∏ ‚äô r‚ÇÜ &&
                    ((ùî´Ã∏ r‚ÇÑ && (ùî´ r‚ÇÇ || r·¥∏ ‚äô r‚ÇÉ‚ãÖr‚ÇÖ)) || r·¥∏ ‚äô r‚ÇÉ) &&
                    ((ùî´Ã∏ r‚ÇÉ && (ùî´ r‚ÇÇ || r·¥∏ ‚äô r‚ÇÑ‚ãÖr‚ÇÖ)) || r·¥∏ ‚äô r‚ÇÑ)

