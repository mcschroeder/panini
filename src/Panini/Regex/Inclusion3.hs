-- | The set of letters that can occur first in a word in the language, i.e.,
-- first(r) = {l ‚àà Œ£ ‚à£ ‚àÉw: l‚ãÖw ‚àà L(r)} (Hovland 2012, Table 1).
first :: Regex -> CharSet
first = \case
  One             -> mempty
  Lit p           -> p
  Times1 r1 r2 
    | nullable r1 -> first r1 <> first r2
    | otherwise   -> first r1
  Plus1 r1 r2     -> first r1 <> first r2
  Star r          -> first r
  Opt r           -> first r

